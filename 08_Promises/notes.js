/***
 * what is the Promise ?
 * A Promise is an object that represents a value, that might not be available yet but will 
 * be at some point in the future.
 * Promises are used to handle asynchronous operations, such as fetching data from an API, reading a file, 
 * or making a database query.
 * A Promise can be in one of three states:
 * Pending: 
 * The initial state, representing the promise's state before it's resolved or rejected.
 * Fulfilled: 
 * The state indicating that the asynchronous operation was completed successfully, resulting in a resolved promise.
 * Rejected: 
 * The state indicating that the asynchronous operation encountered an error or was unsuccessful, leading to a rejected promise.;
 * How to create a Promise ?
 * Promise can be created using Promise constructor  
 * cont promise = new Promise();
 */